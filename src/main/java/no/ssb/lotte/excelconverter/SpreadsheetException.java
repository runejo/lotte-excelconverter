package no.ssb.lotte.excelconverter;

public class SpreadsheetException extends Exception {
    /**
     * Constructs an {@code SpreadsheetException} with the specified detail message.
     *
     * @param message
     *        The detail message (which is saved for later retrieval
     *        by the {@link #getMessage()} method)
     */
    public SpreadsheetException(String message) {
        super(message);
    }

}
